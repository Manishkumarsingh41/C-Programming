// Program to illustrate relational operators.

#include<stdio.h>

void main ()
{   int R,a,b;
    R = (a<b);
    printf("%d\t",R);

    R = (a>b);
    printf("%d\t",R);

    R = (a<=b);
    printf("%d\t",R);

    R = (a>=b);
    printf("%d\t",R);

    R = (a==b);
    printf("%d\t",R);

    R = (a!=b);
    printf("%d\t",R);

}